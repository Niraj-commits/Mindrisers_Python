
# Operators

# Arithmetic operators ("+" , "-" , "/" , "*")

a = 10
b = 5

# addition operator

addition = a + b

# Subtraction Operator

subtraction = a-b

# Division operator

division = a/b #gives value in float

# Modulus Operator

modulus = a%b #gives reminder in return

# Exponential Operator

Exponent = a**b #a to the power of b

# Floor Division

FloorDivision = a//b #gives value in integer

print(addition)
print(subtraction)
print(division)
print(modulus)
print(Exponent)
print(FloorDivision)