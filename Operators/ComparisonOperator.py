
# Equal to comparison operator
num1 = 10
num2 = 15

print(num1 == num2)

# Greater Than Operator

print(num1 > num2)

# Less than operator

print(num1 < num2)

# Greater than Equal to Operator

print (num1 >= num2)

# Less than Equal to Operator

print(num1 <= num2)

# Not equal to operator

print(num1 != num2)